package day07;

public class RelationalOperator {
    public static void main(String[] args) {
        //RELATIONAL OPERATOR

        //>  >=  <  <=  !=

        System.out.println(7>5);
        System.out.println(7<-19);
        System.out.println(5>=5);
        System.out.println(7>=8);
        System.out.println(4<=5);
        System.out.println(17>=8);

        System.out.println(5==5); //checks for equality
        // ==  says it IS equal to each other????
        System.out.println(6!=10); //6 is not equal to 10 prints TRUE
        // !=  it says:  it is not equal to each other??
        System.out.println(6!=6);// prints FALSE

    }
}
