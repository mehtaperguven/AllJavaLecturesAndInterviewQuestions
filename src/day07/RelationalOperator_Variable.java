package day07;

public class RelationalOperator_Variable {
    public static void main(String[] args) {
        //RELATIONAL OPERATOR

        //>  >=  <  <=  !=
int myNumber=10;
        System.out.println(myNumber>5);
        System.out.println(myNumber<-19);
        System.out.println(myNumber>=5);
        System.out.println(myNumber>=8);
        System.out.println(myNumber<=5);
        System.out.println(myNumber>=8);

        System.out.println(myNumber==5); //checks for equality
        // ==  says it IS equal to each other????
        System.out.println(myNumber!=10); //6 is not equal to 10 prints TRUE
        // !=  it says:  it is not equal to each other??
        System.out.println(myNumber!=6);// prints FALSE

    }
}
