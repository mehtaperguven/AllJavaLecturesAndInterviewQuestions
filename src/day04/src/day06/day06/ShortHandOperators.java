package day04.src.day06.day06;


public class ShortHandOperators {
    public static void main(String[] args) {
        // shorthand operators to simplify the operations!!!!!
        //+=, -=, *=, /=, %=
        int studentOnLine= 263;
        studentOnLine+=5;
        //studentOnLine=studentOnLine+5; same with the previous line
        System.out.println("5 people joined the class  :"+ studentOnLine);
        studentOnLine-=3;
        System.out.println("3 people left the class  :"+ studentOnLine);

        studentOnLine*=2;
        System.out.println("The numbers of students doubled "+ studentOnLine);

        studentOnLine/=3;
        System.out.println("numbers that dropped to 1/3 :"+ studentOnLine);





    }


}
