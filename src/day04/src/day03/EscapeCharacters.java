package day04.src.day03;

public class EscapeCharacters {
    public static void main(String[] args) {
        System.out.println("Hello \t world"); //writes to the next tab
        System.out.println("Hello \nworld");//writes to the next line
        System.out.println("Hello \' world");
        System.out.println("Hello \" world");
        System.out.println("Hello \\ world");

        System.out.println("I like the book \"Java\" awesome"); //to use quotes symbols  in the line

        System.out.println("The movie name is : \'Lord of the rings\' ");
        System.out.println("Hello\tWorld");
        System.out.println("Hello\nB15");
        //what is the equivalent of println and print, using one of the above

        System.out.print("tis is regular print \n");
        System.out.println("This is println");


        System.out.println("I\n"); //writes to the next tab
        System.out.println("L \n");//writes to the next line
        System.out.println("O \n");
        System.out.println("V \n");
        System.out.println("E \n");
        System.out.println("D\n"); //writes to the next tab
        System.out.println("I \n");//writes to the next line
        System.out.println("D \n");
        System.out.println("E\n");
        System.out.println("M ");



    }
}
