package day04.src.day04;

public class ArithmeticOperator {
    public static void main(String[] args) {

        // arithmetic operator between two int will be --->> int
        System.out.println( 5+3);
        System.out.println( 5-3);
        System.out.println( 5*3);
        System.out.println( 5/3);
        System.out.println( "\n");// it writes to the next line!!!!!
        //Arithmetic operation between two numerical data type
        //The final result will be larger data type
        System.out.println( 5+3.0d);
        System.out.println( 5-3.0d);
        System.out.println( 5*3.0d);
        System.out.println( 5/3.0d);
        System.out.println( 5/3.0f);
    }
}
